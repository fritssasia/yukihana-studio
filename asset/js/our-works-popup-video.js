// Our Works PopUp Video
$(document).ready(function () {
    var url = $(".ourWorksVideo").attr('src');
    $(".ourWorksPopUpVideo01").on('hide.bs.modal', function () {
        $(".ourWorksVideo").attr('src', '');
    });
    $(".ourWorksPopUpVideo01").on('show.bs.modal', function () {
        $(".ourWorksVideo").attr('src', url);
    });
});

